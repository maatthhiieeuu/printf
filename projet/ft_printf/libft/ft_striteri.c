#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i] != '\0')
	{
		f(i, &s[i]);
		i++;
	}
}
//	striteri est utilise pour parcourir une chaine de caractere et 
//	appliquer une fonction personnalise sur chaque octet de la chaine pour 
//	la transformer.
